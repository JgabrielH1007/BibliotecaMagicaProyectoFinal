    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String titulo = jTextField1.getText().trim();

        if (titulo.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "âš ï¸ Ingrese el tÃ­tulo del libro que desea eliminar.",
                "Campo vacÃ­o", JOptionPane.WARNING_MESSAGE);
            return;
        }

        NodoAVL nodo = biblio.getArbolTitulo().buscar(titulo);
        if (nodo == null || nodo.getLibro() == null) {
            JOptionPane.showMessageDialog(this,
                "âŒ No se encontrÃ³ ningÃºn libro con el tÃ­tulo: " + titulo,
                "No encontrado", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Libro libro = nodo.getLibro();

        int confirm = JOptionPane.showConfirmDialog(this,
            "Â¿Desea eliminar el libro \"" + libro.getTitulo() + "\"?",
            "Confirmar eliminaciÃ³n", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // ðŸ§¾ Verificar si hay mÃ¡s copias
        if (libro.getNumCopias() > 1) {
            libro.setNumCopias(libro.getNumCopias() - 1);
            JOptionPane.showMessageDialog(this,
                "ðŸ“˜ Se eliminÃ³ una copia de \"" + libro.getTitulo() + "\".\n" +
                "Copias restantes: " + libro.getNumCopias(),
                "Copia eliminada", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        biblio.getCatalogo().eliminarLibro(libro.getTitulo());
        biblio.getArbolTitulo().eliminar(libro.getTitulo());
        biblio.getArbolAnio().eliminar(libro.getAnio(), libro);
        biblio.getArbolGenero().eliminarPorTitulo(libro.getTitulo());
        biblio.getTabla().eliminar(libro.getIsnbNum());

        JOptionPane.showMessageDialog(this,
            "âœ… Libro \"" + libro.getTitulo() + "\" eliminado completamente del sistema.",
            "Libro eliminado", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

  
